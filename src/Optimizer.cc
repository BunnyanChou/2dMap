    void Optimizer::AlignGPSImageCeres(KeyFrame *pKF, bool* pbStopFlag, Map* pMap)
{    
    // Local KeyFrames: First Breath Search from Current Keyframe
    list<KeyFrame*> lLocalKeyFrames;

    lLocalKeyFrames.push_back(pKF);
    pKF->mnBALocalForKF = pKF->mnId;

    const vector<KeyFrame*> vNeighKFs = pKF->GetVectorCovisibleKeyFrames();
    for(int i=0, iend=vNeighKFs.size(); i<iend; i++)
    {
        KeyFrame* pKFi = vNeighKFs[i];
        pKFi->mnBALocalForKF = pKF->mnId;
        if(!pKFi->isBad())
            lLocalKeyFrames.push_back(pKFi);
    }

    // Local MapPoints seen in Local KeyFrames
    list<MapPoint*> lLocalMapPoints;
    for(list<KeyFrame*>::iterator lit=lLocalKeyFrames.begin() , lend=lLocalKeyFrames.end(); lit!=lend; lit++)
    {
        vector<MapPoint*> vpMPs = (*lit)->GetMapPointMatches();
        for(vector<MapPoint*>::iterator vit=vpMPs.begin(), vend=vpMPs.end(); vit!=vend; vit++)
        {
            MapPoint* pMP = *vit;
            if(pMP)
                if(!pMP->isBad())
                    if(pMP->mnBALocalForKF!=pKF->mnId)
                    {
                        lLocalMapPoints.push_back(pMP);
                        pMP->mnBALocalForKF=pKF->mnId;
                    }
        }
    }

    // Fixed Keyframes. Keyframes that see Local MapPoints but that are not Local Keyframes
    list<KeyFrame*> lFixedCameras;
    for(list<MapPoint*>::iterator lit=lLocalMapPoints.begin(), lend=lLocalMapPoints.end(); lit!=lend; lit++)
    {
        map<KeyFrame*,size_t> observations = (*lit)->GetObservations();
        for(map<KeyFrame*,size_t>::iterator mit=observations.begin(), mend=observations.end(); mit!=mend; mit++)
        {
            KeyFrame* pKFi = mit->first;

            if(pKFi->mnBALocalForKF!=pKF->mnId && pKFi->mnBAFixedForKF!=pKF->mnId)
            {                
                pKFi->mnBAFixedForKF=pKF->mnId;
                if(!pKFi->isBad())
                    lFixedCameras.push_back(pKFi);
            }
        }
    }


    ceres::Problem problem;
    ceres::Solver::Options options;
    options.linear_solver_type = ceres::SPARSE_NORMAL_CHOLESKY;
    //options.minimizer_progress_to_stdout = true;
    //options.max_solver_time_in_seconds = SOLVER_TIME * 3;
    options.max_num_iterations = 10;
    ceres::Solver::Summary summary;
    ceres::LossFunction *loss_function;
    loss_function = new ceres::HuberLoss(1.0);
    ceres::LocalParameterization* local_parameterization = new ceres::QuaternionParameterization();

    // Set Local KeyFrame vertices
    double scale = 1;
    Eigen::Quaterniond rotation = Eigen::Quaterniond::Identity();
    Eigen::Vector3d translation = Eigen::Vector3d::Zero();
    double scale_ptr = scale;
    // double rotation_ptr[4] = { rotation.w(), rotation.x(), rotation.y(), rotation.z() };
    double auler_angle[3];  // 将旋转矩阵转换为旋转向量（Angle-Axis）
    ceres::RotationMatrixToAngleAxis(rotation.toRotationMatrix().data(), auler_angle);
    double translation_ptr[3] = { translation.x(), translation.y(), translation.z() };
    problem.AddParameterBlock(&scale_ptr, 1.0);
    // problem.AddParameterBlock(rotation_ptr, 4, local_parameterization);
    problem.AddParameterBlock(auler_angle, 3);
    problem.AddParameterBlock(translation_ptr, 3);
    for(list<KeyFrame*>::iterator lit=lLocalKeyFrames.begin(), lend=lLocalKeyFrames.end(); lit!=lend; lit++)
    {
        KeyFrame* pKFi = *lit;

        ceres::CostFunction* gps_function = SimilarityCostFunction::Create(Converter::toVector3d(pKFi->Tgpsc.rowRange(0, 3).col(3)), Converter::toVector3d(pKFi->GetCameraCenter()));
        // problem.AddResidualBlock(gps_function, loss_function, &scale_ptr, rotation_ptr, translation_ptr);
        problem.AddResidualBlock(gps_function, loss_function, &scale_ptr, auler_angle, translation_ptr);
    }

    ceres::Solve(options, &problem, &summary);
    
    std::cout << "*******ceres**************" << std::endl;

    scale = scale_ptr;
    // rotation = Eigen::Quaterniond(rotation_ptr[0], rotation_ptr[1], rotation_ptr[2], rotation_ptr[3]);
    double rotation_est[4];
    ceres::AngleAxisToQuaternion(auler_angle, rotation_est);
    rotation = Eigen::Quaterniond(rotation_est[0], rotation_est[1], rotation_est[2], rotation_est[3]);
    translation = Eigen::Vector3d(translation_ptr[0], translation_ptr[1], translation_ptr[2]);
    
    std::cout << "scale " << scale << std::endl;
    std::cout << "r " << rotation.toRotationMatrix() << std::endl;
    std::cout << "t " << translation << std::endl;
    for(list<KeyFrame*>::iterator lit=lLocalKeyFrames.begin(), lend=lLocalKeyFrames.end(); lit!=lend; lit++)
    {
        KeyFrame* pKF = *lit;
    
        cv::Mat dst_transformed = scale * Converter::toCvMat(rotation.toRotationMatrix()) * pKF->GetCameraCenter() + Converter::toCvMat(translation);   
        std::cout << "pKF tO: " << pKF->GetCameraCenter() << std::endl;
        std::cout << "gps est: " << dst_transformed << std::endl;
        std::cout << "gps: " << pKF->Tgpsc.rowRange(0, 3).col(3) << std::endl;
    }
}

